    public void AutoID(){
        
        try {
            
            pst = con.prepareStatement("select max(pid) from patient");
            rs = pst.executeQuery();
            rs.next();
            
            if( rs.getString("max(pid)") == null){
                pno.setText("1");
            }
            else{
                int id = Integer.parseInt(rs.getString("max(pid)"));
                id++;
                pno.setText(Integer.toString(id));
            }
            
            ismale.setSelected(true);
            isfemale.setSelected(false);
            
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
