    private void ptableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ptableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)ptable.getModel();
        int SelectIndex = ptable.getSelectedRow();
        
        pno.setText(dtm.getValueAt(SelectIndex, 0).toString());
        pname.setText(dtm.getValueAt(SelectIndex, 1).toString());
        
        if(dtm.getValueAt(SelectIndex, 2).equals("Male")){
            ismale.setSelected(true);
        }
        else{
            isfemale.setSelected(true);
        }
        
        email.setText(dtm.getValueAt(SelectIndex, 3).toString());
        phone.setText(dtm.getValueAt(SelectIndex, 4).toString());
        address.setText(dtm.getValueAt(SelectIndex, 5).toString());
        
        jButton1.setEnabled(false);
        
        
        
    }//GEN-LAST:event_ptableMouseClicked

